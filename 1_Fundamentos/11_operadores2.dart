main() {
//Operadores aritiméticos usando a variável para atibuir nela mesma (Binário/Infix)
  double numero1 = 10;
  double numero2 = 5;
  numero1 = numero1 + numero2;
  numero1 -= numero2;
  numero1 *= numero2;
  numero1 /= numero2;
  numero1 %= numero2;

// Operadores relacionais (Binário/Infix)
  double numero3 = 7;
  double numero4 = 2;

  print(numero3 > numero4);
  print(numero3 < numero4);
  print(numero3 >= numero4);
  print(numero3 <= numero4);
  print(numero3 == numero4);
  print(numero3 != numero4);

}
